    public void update(){
         try{ 
           Statement stat = CC.createStatement();
           stat.executeUpdate("UPDATE petugas INNER JOIN User ON petugas.Nik = User.Nis SET Nama = '"+Nama.getText()+"',Nik = '"+Nik.getText()+"', Alamat = '"+Alamat.getText()+"', NoHp = '"+NoHp.getText()+"', Email = '"+Email.getText()+"', User.Role = '"+ro+"' WHERE petugas.Nik = '"+NIK.getText()+"'");
           JOptionPane.showMessageDialog(null, "Data Petugas Berhasil Diupdate");
       }catch (Exception e){
       e.printStackTrace();
       }
    }
    public void delete(){
         try{ 
           Statement stat = CC.createStatement();
           stat.executeUpdate("DELETE petugas FROM petugas INNER JOIN User ON petugas.Nik = User.Nis WHERE petugas.Nik = '"+NIK.getText()+"'");
           JOptionPane.showMessageDialog(null, "Data Petugas Berhasil Dihapus");
       }catch (Exception e){
       e.printStackTrace();
       }
    }
